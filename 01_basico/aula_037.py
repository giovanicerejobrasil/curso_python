"""
Operadores de atribuição
= += -= *= /= //= **= %=
"""

contador = 0
print(contador)

contador += 10
print(contador)

contador -= 2
print(contador)

contador *= 3
print(contador)

contador /= 2
print(contador)

contador **= 5
print(contador)

contador //= 3
print(contador)

contador %= 2
print(contador)

a = 'A'
a += 'B'
print(a)

