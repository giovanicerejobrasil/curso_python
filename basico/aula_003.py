"""
Python => Linguagem de Programação
Tipo de tipagem => Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
# Aspas simples
print('Hello, World!')
print('Giovani "Gio" Brasil')

# Aspas duplas
print("Hello, World!")
print("Giovani 'Gio' Brasil")

# Escape
print("Giovani \"Gio\" Brasil")

# r
print(r"Giovani \"Gio\" Brasil")
